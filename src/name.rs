use core::fmt::Debug;

use num_enum::{IntoPrimitive, TryFromPrimitive};

/// Name of J1939 node, consists of a 64-bit value
#[derive(Debug, Clone, Copy, PartialEq)]
pub struct Name {
    /// 1-bit Arbitrary Address Capable
    /// Indicate the capability to solve address conflicts.
    /// Set to 1 if the device is Arbitrary Address Capable, set to 0 if
    /// it's Single Address Capable.
    pub address_capable: bool,
    /// 3-bit Industry Group
    /// One of the predefined J1939 industry groups.
    pub industry_group: u8,
    /// 4-bit Vehicle System Instance
    /// Instance number of a vehicle system to distinguish two or more
    /// device with the same Vehicle System number in the same J1939
    /// network.
    /// The first instance is assigned to the instance number 0.
    pub vehicle_system_instance: u8,
    /// 7-bit Vehicle System
    /// A subcomponent of a vehicle, that includes one or more J1939
    /// segments and may be connected or disconnected from the vehicle.
    /// A Vehicle System may be made of one or more functions. The Vehicle
    /// System depends on the Industry Group definition.
    pub vehicle_system: u8,
    /// 8-bit Function
    /// One of the predefined J1939 functions. The same function value
    /// (upper 128 only) may mean different things for different Industry
    /// Groups or Vehicle Systems.
    pub function: u8,
    /// 5-bit Function Instance
    /// Instance number of a function to distinguish two or more devices
    /// with the same function number in the same J1939 network.
    /// The first instance is assigned to the instance number 0.
    pub function_instance: u8,
    /// 3-bit ECU Instance
    /// Identify the ECU instance if multiple ECUs are involved in
    /// performing a single function. Normally set to 0.
    pub ecu_instance: u8,
    /// 11-bit Manufacturer Code
    /// One of the predefined J1939 manufacturer codes.
    pub manufacturer_coder: u16,
    /// 21-bit Identity Number
    /// A unique number which identifies the particular device in a
    /// manufacturer specific way.
    pub identity_number: u32,
}

impl Name {
    pub fn default() -> Self {
        Name {
            address_capable: true,
            industry_group: IndustryGroup::AgriculturalAndForestry.into(),
            vehicle_system_instance: 0,
            vehicle_system: VehicleSystem2Agriculture::Tractor.into(),
            function: Functions::NotAvailable255.into(),
            function_instance: 0,
            ecu_instance: 0,
            manufacturer_coder: ManufacturerCodes::Reserved0.into(),
            identity_number: 0,
        }
    }
}

impl From<u64> for Name {
    fn from(item: u64) -> Self {
        Name {
            address_capable: (item >> 63) & 0x01 > 0,
            industry_group: (((item >> 60) & 0x07) as u8),
            vehicle_system_instance: ((item >> 56) & 0x0F) as u8,
            vehicle_system: ((item >> 49) & 0x7F) as u8,
            function: ((item >> 40) & 0xFF) as u8,
            function_instance: ((item >> 35) & 0x1F) as u8,
            ecu_instance: ((item >> 32) & 0x7) as u8,
            manufacturer_coder: ((item >> 21) & 0x7FF) as u16,
            identity_number: (item & 0x3FFFFF) as u32,
        }
    }
}

impl From<[u8; 8]> for Name {
    fn from(item: [u8; 8]) -> Self {
        Self::from(u64::from_le_bytes(item))
    }
}

impl From<Name> for u64 {
    fn from(name: Name) -> u64 {
        let mut res_value: u64 = 0;
        res_value |= name.identity_number as u64 & 0x3FFFFF;
        res_value |= (name.manufacturer_coder as u64 & 0x7FF) << 21;
        res_value |= (name.ecu_instance as u64 & 0x7) << 32;
        res_value |= (name.function_instance as u64 & 0x1F) << 35;
        res_value |= (name.function as u64 & 0xFF) << 40;
        res_value |= (name.vehicle_system as u64 & 0x7F) << 49;
        res_value |= (name.vehicle_system_instance as u64 & 0x0F) << 56;
        res_value |= (name.industry_group as u64 & 0x07) << 60;
        res_value |= (name.address_capable as u64 & 0x01) << 63;
        res_value
    }
}

impl From<Name> for [u8; 8] {
    fn from(name: Name) -> [u8; 8] {
        let raw: u64 = name.into();
        raw.to_le_bytes()
    }
}

